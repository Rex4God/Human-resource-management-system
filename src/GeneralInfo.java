    public ArrayList<User>userList(){
        
       ArrayList<User>registersList = new  ArrayList<>();
        try{
          
       MyConnection.getConnection();
       con = DriverManager.getConnection("jdbc:mysql://localhost/human resource management","root","");
         
       String sql ="SELECT *FROM register";
       
       Statement st =  con.createStatement();
       ResultSet rs =  st.executeQuery(sql);
        User user;
        while (rs.next()){
            
          user = new User(rs.getInt("ID"),rs.getString("sf_name"), rs.getString("e_mail"), rs.getString("sf_address"), rs.getString("sf_contact"),rs.getString("sf_city"), rs.getString("sex"),rs.getString("DoB"),rs.getString("off_post"),rs.getString("Religion"),rs.getString("Hobbies"),rs.getString("j_type"));
          registersList.add(user);
          
   
        }
        }
        
        catch(Exception e){
          
          JOptionPane.showMessageDialog(null, e);
      }
               
       
       
      return registersList;  
    
    }
